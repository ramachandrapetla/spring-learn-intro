package com.springlearn.spring.learn.intro.game;

public class MarioGame {
	public void up() {
		System.out.println("Jump");
	}
	
	public void down() {
		System.out.println("Go into Hole");
	}
	
	public void left() {
		System.out.println("Go Back");
	}
	
	public void right() {
		System.out.println("Accelerate");
	}
}
